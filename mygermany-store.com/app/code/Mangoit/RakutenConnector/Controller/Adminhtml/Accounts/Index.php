namespace Mangoit\RakutenConnector\Controller\Adminhtml\Accounts;

use Magento\Backend\App\Action\Context;
use Magento\Framework\View\Result\PageFactory;
use Mangoit\RakutenConnector\Controller\Adminhtml\Accounts;

class Index extends Accounts
{
    /**
     * @var \Magento\Framework\View\Result\PageFactory
     */
    private $resultPageFactory;

    /**
     * @param \Magento\Backend\App\Action\Context        $context
     * @param \Magento\Framework\View\Result\PageFactory $resultPageFactory
     */
    public function __construct(
        Context $context,
        PageFactory $resultPageFactory
    ) {
        parent::__construct($context);
        $this->resultPageFactory = $resultPageFactory;
    }

    /**
     * Rakuten Account List Page.
     *
     * @return \Magento\Backend\Model\View\Result\Page
     */
    public function execute()
    {
        /** @var \Magento\Backend\Model\View\Result\Page $resultPage */
        $resultPage = $this->resultPageFactory->create();
        $resultPage->setActiveMenu('Mangoit_RakutenConnector::manage_amazon_accounts');
        $resultPage->getConfig()->getTitle()->prepend(__('Rakuten Seller Account List'));
        return $resultPage;
    }
}
